class Car {
  constructor() {
    this.head = "";
    this.body = "";
    this.wheel = "";
  }

  getHead() {
    return this.head;
  }
  setHead(head) {
    this.head = head;
  }

  getBody() {
    return this.body;
  }
  setBody(body) {
    this.body = body;
  }

  getWheel() {
    return this.wheel;
  }
  setWheel(wheel) {
    this.wheel = wheel;
  }
}

export default Car;
